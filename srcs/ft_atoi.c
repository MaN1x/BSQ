int		ft_atoi(char *str)
{
	int i;
	int res;
	int n;

	i = 0;
	res = 0;
	n = 1;
	while ((str[i] > 8 && str[i] < 14) || str[i] == 32)
		i++;
	if (str[i] == '-')
	{
		n = -1;
		i++;
	}
	else if (str[i] == '+')
		i++;
	while (str[i] >= '0' && str[i] <= '9')
	{
		res = res * 10 + str[i] - '0';
		i++;
	}
	if (res > 2147483647 || res < -2147483648)
		return (0);
	else
		return (res * n);
}
